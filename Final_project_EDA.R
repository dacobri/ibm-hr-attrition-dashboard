# 9. Attrition-Focused EDA: Categorical Variables
# =====================================================

# For each categorical predictor, stacked proportional bar charts are used
# rather than raw count bars. Because pct is computed explicitly before
# plotting, geom_col() with position_stack() is used rather than
# position = "fill", which avoids redundant rescaling and gives direct
# control over label placement. A helper function is defined to avoid
# repetitive code across the seven categorical variables examined.

plot_attrition_cat <- function(data, var, title) {
  data %>%
    count(.data[[var]], Attrition) %>%
    group_by(.data[[var]]) %>%
    mutate(pct = n / sum(n)) %>%
    ggplot(aes(x = .data[[var]], y = pct, fill = Attrition)) +
    geom_col() +
    geom_text(aes(label = scales::percent(pct, accuracy = 0.1)),
              position = position_stack(vjust = 0.5), size = 3, color = "white") +
    scale_y_continuous(labels = percent) +
    scale_fill_manual(values = c("Yes" = "#E74C3C", "No" = "#3498DB")) +
    labs(title = title, x = var, y = "Proportion") +
    theme_minimal() +
    theme(axis.text.x = element_text(angle = 45, hjust = 1))
}


