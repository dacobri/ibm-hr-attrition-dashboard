# =====================================================
# 8. Statistical Tests: Categorical Variables vs Attrition
# =====================================================

# Chi-square tests of independence are used to assess whether the association
# between each categorical predictor and Attrition is statistically
# significant. The null hypothesis (H0) for each test is that the variable
# is independent of attrition status. Cramér's V is computed as an effect
# size measure, ranging from 0 (no association) to 1 (perfect association),
# and allows comparison across variables with different numbers of categories.
# Monte Carlo simulation (simulate.p.value = TRUE, B = 2000) is used rather
# than the asymptotic chi-square approximation because some cell counts are
# small, which violates the standard approximation's assumptions.
# set.seed(123) is set before the block to ensure the simulation results
# are fully reproducible across runs.

# Over18 is excluded from the chi-square analysis: it is a constant variable
# (all employees are "Y") identified in Section 3.6, so the test would produce
# an undefined Cramér's V (division by zero). EDA-engineered columns
# (AgeGroup, IncomeGroup, AvgSatisfaction) are also excluded as they are
# derived variables, not original predictors. Only the seven original
# categorical predictors are tested.
set.seed(123)
cat_var_names <- c("BusinessTravel", "Department", "EducationField",
                   "Gender", "JobRole", "MaritalStatus", "OverTime")

chi_sq_results <- map_dfr(cat_var_names, function(var) {
  tbl <- table(df[[var]], df$Attrition)
  test <- chisq.test(tbl, simulate.p.value = TRUE, B = 2000)
  n <- sum(tbl)
  k <- min(nrow(tbl), ncol(tbl))
  cramers_v <- sqrt(test$statistic / (n * (k - 1)))
  tibble(
    Variable  = var,
    chi_sq    = round(test$statistic, 2),
    p_value   = signif(test$p.value, 3),
    cramers_v = round(cramers_v, 3),
    significant = ifelse(test$p.value < 0.05, "Yes", "No")
  )
})

cat("\nChi-square tests: Categorical variables vs Attrition\n")
print(chi_sq_results %>% arrange(desc(cramers_v)))

# Results ranked by Cramér's V:
#   - OverTime: p < 0.001, V = 0.246: the strongest categorical predictor.
#     We reject H0: overtime status is significantly associated with attrition.
#   - JobRole: p < 0.001, V = 0.242: nearly as strong as OverTime; job role
#     differentiates attrition rates substantially across the nine categories.
#   - MaritalStatus: p < 0.001, V = 0.177: significant; single employees
#     show markedly higher attrition than married or divorced peers.
#   - BusinessTravel: p < 0.001, V = 0.128: significant; frequent travellers
#     have meaningfully higher attrition than non-travellers.
#   - EducationField: p = 0.009, V = 0.104: significant but modest effect.
#   - Department: p = 0.0045, V = 0.086: significant; weaker than job role,
#     which captures the same variance more granularly.
#   - Gender: p = 0.258 > 0.05: we fail to reject H0; gender shows no
#     statistically significant association with attrition at the 5% level.
# All significant categorical predictors are included in the ML feature set.


