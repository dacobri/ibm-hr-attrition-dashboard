# ----- 6.2 IQR outlier summary -----

outlier_summary <- lapply(names(num_vars_box), function(col) {
  x  <- df[[col]]
  q1 <- quantile(x, 0.25, na.rm = TRUE)
  q3 <- quantile(x, 0.75, na.rm = TRUE)
  iqr <- q3 - q1
  lower <- q1 - 1.5 * iqr
  upper <- q3 + 1.5 * iqr
  outliers <- sum(x < lower | x > upper, na.rm = TRUE)
  data.frame(
    variable     = col,
    outliers     = outliers,
    pct_outliers = round(outliers / nrow(df) * 100, 1)
  )
}) %>% bind_rows()

outlier_summary %>% arrange(desc(outliers))

# The IQR rule (flag values below Q1 − 1.5×IQR or above Q3 + 1.5×IQR) is
# applied programmatically across all numeric variables. The highest outlier
