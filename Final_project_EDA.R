# =====================================================
# 12. Attrition Rate by Category (Ranking View)
# =====================================================

# Ranked horizontal bar charts of attrition rates are used to communicate
# the relative risk across categories in a format readily interpretable by
# HR stakeholders. Proportional rates rather than raw counts are shown because
# they are directly comparable across groups of different sizes. A helper
# function avoids code repetition across the five categorical variables.

plot_attrition_rate <- function(data, group_var, title) {
  data %>%
    group_by(.data[[group_var]]) %>%
    summarise(
      employees      = n(),
      attrition_rate = mean(Attrition == "Yes"),
      .groups = "drop"
    ) %>%
    arrange(desc(attrition_rate)) %>%
    ggplot(aes(x = reorder(.data[[group_var]], attrition_rate),
               y = attrition_rate)) +
    geom_col(fill = "steelblue") +
    geom_text(aes(label = paste0(round(attrition_rate * 100, 1), "%")),
              hjust = -0.1, size = 3) +
    scale_y_continuous(labels = percent,
                       expand = expansion(mult = c(0, 0.15))) +
    coord_flip() +
    labs(title = title, x = NULL, y = "Attrition Rate") +
    theme_minimal()
}

plot_attrition_rate(df, "JobRole",        "Attrition Rate by Job Role")
plot_attrition_rate(df, "Department",     "Attrition Rate by Department")
plot_attrition_rate(df, "BusinessTravel", "Attrition Rate by Business Travel")
plot_attrition_rate(df, "EducationField", "Attrition Rate by Education Field")
plot_attrition_rate(df, "MaritalStatus",  "Attrition Rate by Marital Status")

# These charts complement Section 9 by providing a clean ranked view across
# all categories of each variable simultaneously. A key nuance is that the
# highest-rate group is not always where intervention resources are most
# efficiently deployed: a 40% attrition rate in a 10-person group represents
# 4 departures; a 15% rate in a 200-person group represents 30 departures and
# a larger total organisational impact. Additionally, groups close to the
# overall average attrition rate (~16%) are more likely to contain employees
# who are "on the edge" and responsive to intervention, whereas very
# high-rate groups may include employees with strong external pulls (e.g.,
# Sales Reps with competing offers) where retention is structurally more
# difficult.


